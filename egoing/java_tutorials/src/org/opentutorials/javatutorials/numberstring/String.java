package org.opentutorials.javatutorials.numberstring;

public class String {

	public static void main(java.lang.String[] args) {
		System.out.println('생');
		// System.out.println('생활코딩'); // 에러가 발생한다.
		System.out.println("생활코딩");
		System.out.println("생활" + "코딩" + "입니다.");
		System.out.println(1+1);
		System.out.println("1"+"1");
		System.out.println("1"+1);
		
		// System.out.println("egoing said "welcome programming world""); // 에러발생
		System.out.println("egoing said \"welcome programming world\""); // 이스케이프 사용
		System.out.println("egoing said \n\"welcome programming world\""); // 줄바꿈 
	}

}
